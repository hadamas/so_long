#include "ft_printf.h"

int	ft_putunsigned(unsigned int n)
{
	unsigned int	count;

	count = 0;
	if (n >= 10)
		count += ft_putunsigned(n / 10);
	count += ft_putchar((n % 10) + 48);
	return (count);
}
